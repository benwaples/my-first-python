happy = 'i am happy'

# python can get a -value for index and start from end of list
print(happy[-2])

# this will print the characters from 0-3, excluding index 3 though
print(happy[0:3])

# print everything after the first index
print(happy[1:])

# print everthing before the 3rd index
print(happy[:3])

